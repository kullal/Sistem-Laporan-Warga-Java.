    private void inisialisasiTabelModel() {
        modelTabelLaporan = new DefaultTableModel(
            new Object[][] {},
            new String[] {"ID", "NIK Pelapor", "Jenis", "Detail", "Tgl Kejadian", "Tgl Lapor", "Status", "Pgw Penjawab", "Tgl Update"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        TabelLaporanDesa.setModel(modelTabelLaporan);
    }

    private void loadDataLaporan() {
        modelTabelLaporan.setRowCount(0);
        List<Laporan> daftarLaporan = laporanController.getListSemuaLaporan();

        for (Laporan lap : daftarLaporan) {
            String detailSingkat = lap.getDetail_laporan();
            if (detailSingkat != null && detailSingkat.length() > 50) {
                detailSingkat = detailSingkat.substring(0, 50) + "...";
            }
            modelTabelLaporan.addRow(new Object[]{
                lap.getId_laporan(),
                lap.getNik_pelapor(),
                lap.getJenis_laporan(),
                detailSingkat,
                lap.getTanggal_kejadian() != null ? lap.getTanggal_kejadian() : "-",
                lap.getTanggal_lapor() != null ? lap.getTanggal_lapor() : "-",
                lap.getStatus_laporan(),
                lap.getNip_pegawai_penanggung_jawab() != null ? lap.getNip_pegawai_penanggung_jawab() : "-",
                lap.getTanggal_update_status() != null ? lap.getTanggal_update_status() : "-"
            });
        }
    }

