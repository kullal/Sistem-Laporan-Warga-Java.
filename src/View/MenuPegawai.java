    private void btnCariLaporanActionPerformed(java.awt.event.ActionEvent evt) {                                               
        String searchNik = txtCariLaporan.getText().trim();
        if (searchNik.isEmpty()) {
            // Jika kotak pencarian kosong, tampilkan semua data
            sorter.setRowFilter(null);
        } else {
            try {
                // Buat filter yang hanya mencari di kolom NIK Pelapor (kolom indeks 1)
                RowFilter<DefaultTableModel, Object> nikFilter = RowFilter.regexFilter("(?i)" + searchNik, 1);
                sorter.setRowFilter(nikFilter);
                
                // Jika tidak ada hasil, beri tahu pengguna
                if (TabelLaporanDesa.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(this, 
                        "Tidak ditemukan laporan dengan NIK: " + searchNik, 
                        "Pencarian Tidak Ditemukan", 
                        JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (java.util.regex.PatternSyntaxException e) {
                // Jika pola regex tidak valid
                JOptionPane.showMessageDialog(this, 
                    "Format pencarian tidak valid", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
                sorter.setRowFilter(null);
            }
        }
    }  

