    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // Mendapatkan baris yang dipilih
        int selectedRow = TabelLaporanDesa.getSelectedRow();
        if (selectedRow == -1) {
            // Jika tidak ada baris yang dipilih
            JOptionPane.showMessageDialog(this, 
                "Pilih laporan yang ingin diupdate statusnya terlebih dahulu!", 
                "Tidak Ada Baris Dipilih", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Konversi indeks baris model jika tabel di-sort/filter
        int modelRow = TabelLaporanDesa.convertRowIndexToModel(selectedRow);
        
        // Mendapatkan ID Laporan dari baris yang dipilih (kolom 0)
        int idLaporan = Integer.parseInt(modelTabelLaporan.getValueAt(modelRow, 0).toString());
        
        // Mendapatkan status baru dari ComboBox
        String statusBaru = ComboStatusLaporan.getSelectedItem().toString();
        
        // Mendapatkan NIP pegawai yang login
        String nipPegawai = LoginPegawai.getNipPegawaiLogin();
        if (nipPegawai == null || nipPegawai.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "NIP Pegawai tidak diketahui. Mohon login ulang.", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Konfirmasi update
        int confirm = JOptionPane.showConfirmDialog(this,
            "Apakah Anda yakin ingin mengubah status laporan ID " + idLaporan + " menjadi " + statusBaru + "?",
            "Konfirmasi Update Status",
            JOptionPane.YES_NO_OPTION);
            
        if (confirm == JOptionPane.YES_OPTION) {
            // Panggil controller untuk update status
            boolean berhasil = laporanController.prosesUpdateStatus(idLaporan, statusBaru, nipPegawai, this);
            
            if (berhasil) {
                // Update status di model tabel langsung tanpa meload ulang seluruh data
                // Ini akan mempertahankan tampilan tabel dan tidak "menghilangkan" tabel
                modelTabelLaporan.setValueAt(statusBaru, modelRow, 6); // Kolom 6 adalah Status
                
                // Update kolom pegawai penanggung jawab jika kosong sebelumnya
                if ("-".equals(modelTabelLaporan.getValueAt(modelRow, 7).toString())) {
                    modelTabelLaporan.setValueAt(nipPegawai, modelRow, 7); // Kolom 7 adalah Pgw Penjawab
                }
                
                // Update kolom tanggal update
                java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                String tanggalUpdateStr = sdf.format(new java.util.Date());
                modelTabelLaporan.setValueAt(tanggalUpdateStr, modelRow, 8); // Kolom 8 adalah Tgl Update
                
                // Tampilkan pesan sukses
                JOptionPane.showMessageDialog(this,
                    "Status laporan ID " + idLaporan + " berhasil diupdate menjadi " + statusBaru,
                    "Update Berhasil",
                    JOptionPane.INFORMATION_MESSAGE);
                
                // Pastikan baris tetap terpilih setelah update
                TabelLaporanDesa.setRowSelectionInterval(selectedRow, selectedRow);
            }
            // Jika gagal, pesan error sudah ditampilkan oleh controller
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = TabelLaporanDesa.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih laporan yang ingin dihapus terlebih dahulu!", "Tidak Ada Baris Dipilih", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int modelRow = TabelLaporanDesa.convertRowIndexToModel(selectedRow);
        int idLaporan = Integer.parseInt(modelTabelLaporan.getValueAt(modelRow, 0).toString());
        int confirm = JOptionPane.showConfirmDialog(this,
            "Apakah Anda yakin ingin menghapus laporan ID " + idLaporan + "?",
            "Konfirmasi Hapus Laporan",
            JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            boolean sukses = laporanController.hapusLaporan(idLaporan, this);
            if (sukses) {
                loadDataLaporan(); // Refresh tabel setelah hapus
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

